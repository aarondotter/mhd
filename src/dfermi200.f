      FUNCTION DFERMI200(IB, DK, ETA, BETA)
C
C  To compute generalized Fermi-Dirac function of
C  index DK, with degeneracy parameter ETA and relativity
C  parameter BETA.
C
C  PARAMETERS
C       ON ENTRY
C              IB        - Integer
C                          Index for function to be evaluated
C
C              DK        - Double precision
C                          Index of the Fermi-Dirac function.
C
C              ETA       - Double precision
C                          Degeneracy parameter.
C
C              BETA      - Double precision
C                          Relativity parameter.
C
C       ON RETURN
C              DFERMI200 - Double precision
C                          Approximation to the GFD integral.
C                          Result is computed by applying three
C                          200-POINT GAUSS-LEGENDRE and one
C                          200-POINT GAUSS-LAGUERRE RULES over
C                          four appropriate subintervals.
C                          Within each subinterval the FD kernel
C                          is reasonably close to the gaussian
C                          kernel times a simple polynomial.
C
C  REFERENFE:
C      Aparicio J.M., ApJS, 117, 627-632, 1998.
C
      DOUBLE PRECISION DFERMI200,DFERMI_1,DFERMI_2,DK,ETA,BETA
      DOUBLE PRECISION D,SG,A1,B1,C1,A2,B2,C2,D2,E2,A3,B3,C3,D3,E3
      DOUBLE PRECISION ETA1,XI,XI2,X1,X2,X3,S1,S2,S3,S12,
     +                 RES1,RES2,RES3,RES4
      DOUBLE PRECISION PAR(3)
      EXTERNAL DFERMI_1,DFERMI_2
C
C   Parameters defining the location of the breakpoints for the
C   subintervals of integration:
C
      SAVE D,SG,A1,B1,C1,A2,B2,C2,D2,E2,A3,B3,C3,D3,E3
C
      DATA D   / 3.3609D 0 /
      DATA SG  / 9.1186D-2 /
      DATA A1  / 6.7774D 0 /
      DATA B1  / 1.1418D 0 /
      DATA C1  / 2.9826D 0 /
      DATA A2  / 3.7601D 0 /
      DATA B2  / 9.3719D-2 /
      DATA C2  / 2.1063D-2 /
      DATA D2  / 3.1084D 1 /
      DATA E2  / 1.0056D 0 /
      DATA A3  / 7.5669D 0 /
      DATA B3  / 1.1695D 0 /
      DATA C3  / 7.5416D-1 /
      DATA D3  / 6.6558D 0 /
      DATA E3  /-1.2819D-1 /
C
C           LIST OF MAJOR VARIABLES
C           -----------------------
C
C           ETA1   - LINEAR MAP OF ETA
C           XI     - SMOOTH MAP OF ETA ONTO (0,+INF)
C           S_i    - BREAKPOINTS
C           RES_i  - RESULTS OF THE 200-POINT GAUSS FORMULAE
C
C***FIRST EXECUTABLE STATEMENT  DFERMI200
C
C   Integrand parameters:
C
      PAR(1)=DK
      PAR(2)=ETA
      PAR(3)=BETA
C
C   Definition of XI:
C
      ETA1=SG*(ETA-D)
      IF (ETA1.LE.5.D1) THEN
        XI=log(1.D0+exp(ETA1))/SG
      ELSE
        XI=ETA-D
      ENDIF
      XI2=XI*XI
C
C   Definition of the X_i:
C
      X1=(A1  +B1*XI+C1*   XI2)
     +  /(1.D0+C1*XI)
      X2=(A2  +B2*XI+C2*D2*XI2)
     +  /(1.D0+E2*XI+C2*   XI2)
      X3=(A3  +B3*XI+C3*D3*XI2)
     +  /(1.D0+E3*XI+C3*   XI2)
C
C   Breakpoints:
C
      S1=X1-X2
      S2=X1
      S3=X1+X3
      S12=DSQRT(S1)
C
C   Quadrature integrations:
C
      CALL DQLEG200(IB,DFERMI_2,0.D0, S12,RES1,PAR,3)
      CALL DQLEG200(IB,DFERMI_1,  S1,  S2,RES2,PAR,3)
      CALL DQLEG200(IB,DFERMI_1,  S2,  S3,RES3,PAR,3)
      CALL DQLAG200(IB,DFERMI_1,  S3,1.D0,RES4,PAR,3)
      DFERMI200=RES1+RES2+RES3+RES4
      RETURN
      END


